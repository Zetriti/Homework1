//Цикл for
console.log('Цикл for');
for (let i = 0; i < 2; i++) {
  console.log('Привет');
}

//Цикл while
console.log('Цикл while');
let i = 0;
while (i < 2) {
  console.log('Привет');
  i++;
}

//Цикл do while
console.log('Цикл do while');
let a = 0;
do {
  console.log('Привет');
  a++;
} while (a < 2);